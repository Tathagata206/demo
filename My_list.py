my_list = [10, "hello", 3.14, True, "Python"]
print(my_list)
#Replace the 2nd element
my_list[1]="Goodbye"
print(my_list)
#Append a new item
my_list.append("Goodbye")
print(my_list)
#Count a similar item
x=my_list.count("Goodbye")
print(x)
#Sorting the list
my_list1 = [5.2,3,7,10,8]
